<script>
    document.addEventListener("DOMContentLoaded", function () {
        const scheduleData = {
            '2025-01-25': {
                dental: [
                    { name: 'Angelina Class', duration: '01h' },
                    { name: 'Michael Smith', duration: '30m' }
                ],
                implant: [{ name: 'Dr. John Doe', duration: '45m' }],
                oral: [{ name: 'William Class', duration: '30m' }]
            }
        };

        let selectedDate = '2025-01-25';
        let selectedSpeciality = 'dental';

        flatpickr("#calendar", {
            inline: true,
            defaultDate: selectedDate,
            onDayCreate: function (dObj, dStr, fp, dayElem) {
                const dateStr = dayElem.dateObj.toISOString().split('T')[0];
                if (scheduleData[dateStr]) {
                    dayElem.classList.add("available");
                } else {
                    dayElem.classList.add("booked");
                }
            },
            onChange: function (selectedDates, dateStr) {
                selectedDate = dateStr;
                document.getElementById('selected-date').textContent = new Date(dateStr).toDateString();
                updateSchedule();
            }
        });

        document.querySelectorAll('.tab-button').forEach(button => {
            button.addEventListener('click', () => {
                document.querySelectorAll('.tab-button').forEach(btn => btn.classList.remove('active'));
                button.classList.add('active');
                selectedSpeciality = button.getAttribute('data-speciality');
                updateSchedule();
            });
        });

        function updateSchedule() {
            const scheduleList = document.getElementById('schedule-list');
            scheduleList.innerHTML = '';
            const schedules = scheduleData[selectedDate] && scheduleData[selectedDate][selectedSpeciality];
            if (schedules && schedules.length > 0) {
                schedules.forEach(item => {
                    const div = document.createElement('div');
                    div.className = `schedule-item ${selectedSpeciality}`;
                    div.innerHTML = `<span>${item.name}</span><span>${item.duration}</span>`;
                    scheduleList.appendChild(div);
                });
            } else {
                scheduleList.innerHTML = '<p>No schedules available.</p>';
            }
        }

        // Initial setup
        document.getElementById('selected-date').textContent = new Date(selectedDate).toDateString();
        updateSchedule();
    });
</script>

