<script>
let currentIndex = 0;

const tabsContainer = document.querySelector('.tabs-container');
const totalTabs = document.querySelectorAll('.tab-btn2').length;
const tabWidth = document.querySelector('.tab-btn2').offsetWidth + 10; // 10px is the margin-right

// Function to handle the sliding action
function moveSlide(direction) {
    // Update currentIndex by direction (left: -1, right: 1)
    currentIndex += direction;

    // Prevent moving past the first tab on left and past the last tab on right
    if (currentIndex < 0) {
        currentIndex = 0; // Stay on the first tab
    } else if (currentIndex >= totalTabs) {
        currentIndex = totalTabs - 1; // Stay on the last tab
    }

    // Move the container by the width of one tab (tabWidth)
    tabsContainer.style.transform = `translateX(-${currentIndex * tabWidth}px)`;
}

// Dynamically load more tabs if needed (optional)
function loadMoreTabs() {
    const newTab = document.createElement('button');
    newTab.classList.add('tab-btn2');
    newTab.innerHTML = 'New Tab <br> <span class="million">4M+ learners</span>';
    tabsContainer.appendChild(newTab);
}

// Handle the tab click (optional, to show different content for each tab)
function showTab(button, tabId) {
    const allTabs = document.querySelectorAll('.tab-btn2');
    allTabs.forEach(tab => tab.classList.remove('active'));
    button.classList.add('active');
}


</script>

